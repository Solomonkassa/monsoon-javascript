// above can be replaced with person object.

let person = {
    // properties or keys
    name: 'xoraus',
    age: 25
};

console.log(person);

// Dot Notations
person.name = 'forestwolf'

console.log(person.name)

// Bracket Notations

person['name'] = 'forestfox';

console.log(person.name);

// more advance bracket notation

let selection = 'name';

person[selection] = 'bear';

console.log(person.name);



